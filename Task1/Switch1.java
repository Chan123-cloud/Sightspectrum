package Task1;

public class Switch1 {
			public static void main(String[]args){
			char c='a';
			switch(c){
			case'a':
			case'e':
			case'i':
			case'o':
			case'u':
			case'A':
			case'E':
			case'I':
			case'O':
			case'U':
			System.out.println("Character is a vowel");
			break;
			default:
			System.out.println("Character is a consonant");
	}
	}
}


